let num1 = 1
let num2 = 2

num1++ // pos-fixada
       // acrescenta uma unidade ao valor da variavel
console.log(num1)

--num1 // pre-fixada (precedencia maior)
       // subtrai uma unidade ao valor da variavel

console.log(++num1 === num2--) // true
console.log(num1 === num2)     // false

