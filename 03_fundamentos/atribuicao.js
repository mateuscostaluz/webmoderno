const a = 7
let b = 3

b += a // atribuição aditiva (junto com a atribuição faz-se a adição), ou seja, b = b + a
console.log(b)

b -= 4 // atribuição subtrativa, ou seja, b = b - 4
console.log(b)

b *= 2 // atribuição multiplicativa, ou seja, b = b * 2
console.log(b)

b /= 2 // atribuição divisiva, ou seja, b = b / 2
console.log(b)

b %= 2 // atribuição modular, ou seja, b = b % 2
console.log(b)

// símbolo aritimético antes e a atribuição depois